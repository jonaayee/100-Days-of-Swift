import UIKit

// Arrays are a collection of values stored as a single value, typically for simplity

let john = "John Lennon"
let paul = "Paul McCartney"
let george = "George Harrison"
let ringo = "Ringo Starr"

let beatles: [String] = [john, paul, george, ringo]
// You could also put the names inside the array to make it more efficent

beatles[1] // this calls paul as swift starts an array at 0

