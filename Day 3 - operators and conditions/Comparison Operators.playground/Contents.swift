import UIKit

let firstScore = 6
let secondScore = 8

firstScore == secondScore // returns false as "==" means equal (not if you're setting something to something)
firstScore != secondScore // returns true as "!=" means not equal

firstScore < secondScore // returns true as 8 is bigger than 6

firstScore > secondScore // returns false as 6 is not bigger than 8

// Each operator can have an equal sign after it too include if it's the same value "<=" ">="
